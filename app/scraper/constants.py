def generate_cookie():
    cookie_values = []
    for _ in range(23):
        cookie_values.append(str(random.randint(1, 99)))
        cookie = '; '.join([f'{c}={v}' for c, v in zip(
            string.ascii_uppercase, cookie_values)])
    return cookie


